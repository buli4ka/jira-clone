const COMMON_TABLE = data[0].columnTitle;

const getCard = ({ input: cardInput }) => {
  // const columnTitle = data.find(({ columnTitle: t }) => t === cardInput?.columnTitle) ? cardInput?.columnTitle : COMMON_TABLE;

  return data[0].cards[0];
};

const createCard = ({ input: cardInput }) => {
  const id = uuidv4();
  const createdAt = Date.now();

  const columnTitle = data.find(({ columnTitle: t }) => t === cardInput?.columnTitle) ? cardInput?.columnTitle : COMMON_TABLE;
  const description = cardInput?.description ?? '';
  const title = cardInput?.title;
  const card = {
    id, createdAt, columnTitle, description, title
  };

  const index = data.findIndex(({ columnTitle: c }) => c === columnTitle);

  data[index] = { ...data[index], cards: [...data[index].cards, card] };
  return card;
};

const moveCard = ({ input: moveCardInput }) => {
  const fromIndex = data.findIndex(({ columnTitle: c }) => c === moveCardInput?.columnTitleFrom);
  const toIndex = data.findIndex(({ columnTitle: c }) => c === moveCardInput?.columnTitleTo);
  const cardIndex = data[fromIndex].cards.findIndex(({ id }) => id.toString() === moveCardInput?.cardId);
  const cardAfterIndex = data[toIndex].cards.findIndex(({ id }) => id.toString() === moveCardInput?.cardAfterId);
  const card = data[fromIndex].cards[cardIndex];
  data[fromIndex].cards.splice(cardIndex, 1);
  if (cardAfterIndex >= 0)
    data[toIndex].cards.splice(cardAfterIndex + 1, 0, card);
  else
    data[toIndex].cards.push(card);

  return card
};

const getColumns = () => data;


const createColumn = ({ input: columnInput }) => {
  const columnTitle = columnInput?.columnTitle;
  if (data.find((col) => col?.columnTitle === columnTitle )) return ;
  const column = {
    columnTitle, cards: []
  };
  data.push(column);
  return column;
};


export default { getColumns, getCard, createCard, createColumn, moveCard };
